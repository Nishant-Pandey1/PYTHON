# slicing a string
# It just helps to print out the LETTERS on the given string
# From next letter of the starting index to the end indexed letter
b = "Nishant Pandey"
print(b[1:5])
# It prints isha

# slicing from a index to end
b = "Nishant Pandey"
print(b[3:])
# It prints isha

b = "Hello, World!"
print(b[-5:-2])
#Negative sign count from the end

